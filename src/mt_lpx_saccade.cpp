/**
 * HammingDistance utility class for efficient bit counting using lookup table
 * Exactly matching the JavaScript onesCount approach from the test files
 */
class HammingDistance {
private:
    static std::array<uint8_t, 65536> onesCount;  // 2^16 lookup table (64KB)
    static bool initialized;
    
public:
    /**
     * Initialize the ones count lookup table
     * Based on makeOnesCountTable() from JavaScript test files
     */
    static void initialize() {
        if (initialized) return;
        
        for (int i = 0; i < 65536; i++) {
            int count = 0;
            int val = i;
            for (int j = 0; j < 16; j++) {  // Count bits in 16-bit value
                if ((val & 1) == 1) {
                    count += 1;
                }
                val >>= 1;
            }
            onesCount[i] = count;
        }
        initialized = true;
    }
    
    /**
     * Count ones in 32-bit value using lookup table
     * Matches JavaScript approach: lower 16 bits + upper 16 bits
     */
    static int popcount32(uint32_t value) {
        return onesCount[value & 0xFFFF] +           // Lower 16 bits
               onesCount[(value >> 16) & 0xFFFF];    // Upper 16 bits
    }
};

